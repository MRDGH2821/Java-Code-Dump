package inheritance;

/**
 * Sample class to be inherited.
 *
 * @class Sample
 */
public class Sample {
  private int number = 3;

  public int getNumber() {
    return this.number;
  }

  public void text() {
    System.out.println("Just like animals");
  }

  public int setNumber(int number) {
    return this.number = number;
  }
}
